#include <iostream>
#include <bits/stdc++.h>
using namespace std;

int main() {
    // Bitwise Operators : 
    // Bitwise AND (&)
    cout << (3 & 5) << endl;

    // Bitwise OR (|)
    cout << (3 | 5) << endl;

    // Bitwise XOR (^)
    // same -> 0
    // diff -> 1
    cout << (3 ^ 5) << endl;

    return 0;
}